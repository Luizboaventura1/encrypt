function encrypt() {
  let messageContainer = document.querySelector('.message')
  let messageUser = document.getElementById('message').value
  let formatMessage = messageUser.replace(/\s/g, '').toLowerCase()

  let encryptedMessage = ''
  
  for(let i = 0; i < formatMessage.length; i++) {
    let verify = encryptedList[formatMessage[i]]
    encryptedMessage += verify
  }

  messageContainer.innerHTML = `<div class="p-3">${encryptedMessage}</div>`
}